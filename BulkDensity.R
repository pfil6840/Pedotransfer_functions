          (1.35128606477631 + 0.00451974677070142 * sand_20_2000 + 
             (sand_20_2000 - 44.652494600432) * ((sand_20_2000 - 44.652494600432) *
                                                   -0.0000613723924995459) + 0.0596420803366252 * log(mid_depth)))
}  